# Exercise 2: Day of the Week
day = int(input("Enter a number from 1-7: "))
if day >= 7:
    day == 7
    print("It's Sunday.")
if day == 1:
    print("It's Monday.")
elif day == 2:
    print("It's Tuesday.")
elif day == 3:
    print("It's Wednesday.")
elif day == 4:
    print("It's Thursday.")
elif day == 5:
    print("It's Friday.")
else:
    print("It's Saturday.")